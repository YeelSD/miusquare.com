            <div class="col-xl-12 col-lg-12 col-md-6 site-content-inner align-self-center order-2  mb-2">

                <p class="text-justify">La principal función de los filtros terminales es evitar la entrada de partículas y microorganismos en zonas sensibles. Con el tiempo, los filtros pueden perder su capacidad de filtrado debido a varios factores, como la acumulación de partículas, daños en los medios filtrantes, o defectos en las juntas. Las pruebas de integridad, como la prueba de penetración de partículas o el test de presión, permiten detectar cualquier anomalía que indique que el filtro ya no está funcionando de manera óptima.</p>
                <p class="text-justify">Puesto que el volumen de aire conducido por los conductos es calculado con diversos propósitos en mente, tales como el enfriamiento o la calefacción adecuada de un espacio, la dilución de un contaminante, o la presurización de un recinto, es de crucial importancia que el volumen trasportado por la red de conductos sea; por lo menos, el calculado y que solo a través de los elementos terminales del sistema HVAC se recolecte o se suministre el aire a los espacios.</p>
                <p class="text-justify">Dicho esto, una medida de la eficiencia del sistema de trasporte es su tasa de fugas. Típicamente las fugas serán encontradas en las juntas constructivas del sistema y; por ende, un sistema que reduzca las juntas constructivas será más hermético que un sistema que presente un número elevado de juntas.</p>
                <p class="text-justify">En la mayoría de los sistemas aplicados, es imposible construir los sistemas de conductos en una sola pieza, por lo que el tipo de material empleado en la construcción de los conductos y su presión de operación son un elemento crítico para considerar para desde el diseño minimizar la posibilidad de fugas.</p>
                <p class="text-justify">La selección del material de construcción para los conductos dependerá mucho del contexto en donde será construido en el sistema. La construcción mediante paneles aislados (PirAlu, P3 Ductal, por ejemplo) actualmente es popular en zonas tropicales y el material favorece la reducción de uniones; sin embargo, en actualización de sistemas, o en la instalación de sistemas nuevos en edificios antiguos, será más conveniente el conducto convencional de acero, debido a su flexibilidad de fabricación. Ambas tecnologías, instalas en forma profesional otorgan tasas bajas de fuga en sus distribuciones.</p>
                <p class="text-justify">Por otra parte, la presión de operación de la red de conductos es de suma importancia y es responsabilidad del diseñador del sistema especificar dicha presión para el constructor y la entidad de pruebas, puesto la tasa de fugas no se relaciona con el flujo conducido por el sistema, sino que es directamente proporcional a la presión. En forma simple, a mayor presión un sistema dado tendrá una mayor tasa de fuga, por lo cual, tampoco los conductos deberán probarse a una presión mayor a la de operación, puesto que es una prueba para indicar la calidad de la instalación, no la resistencia de los materiales.</p>
                <p class="text-justify">Para sistemas de aire acondicionado y ventilación comerciales, la mayoría de sus conductos podrán ser probados bajo los criterios de prueba para conductos de baja presión o clase A, conforme a DW-144, sin exceder los 500 Pa o 2 pulgadas de columna de agua. En nuestra experiencia, las pruebas a una presión entre 100 y 200 Pa cubrirán la mayoría de las aplicaciones HVAC industriales, comerciales y residenciales.</p>
                <p class="text-justify">La ejecución de pruebas de hermeticidad además de ser un indicador directo de la calidad de la instalación de un sistema ayuda a reducir el consumo de energía de este, puesto que las condiciones obtenidas son trasferidas con una gran eficiencia desde y hacia las salas acondicionadas.</p>
                <p class="text-justify">Por otra parte, las pruebas de hermeticidad en conductos de extracción aseguran que los vapores, polvos o gases recolectados son conducidos hasta el punto de tratamiento y deseño de diseño y no son derramados en puntos no controlados del sistema.</p>
                <p class="text-justify"><strong>Al ejecutar pruebas de hermeticidad deben cuidarse los siguientes parámetros:</strong></p>
                <p class="text-justify">• Acceso a la totalidad de la trayectoria del sistema de conductos: permitirá establecer la superficie del conducto y, por ende, la tasa total de fuga. En segundo lugar, en caso de alguna fuga permitirá reparar dicha fuga y corroborar que el conducto opera conforme a diseño.</p>
                <p class="text-justify">• Presión de operación del conducto: acotará el parámetro de presión de prueba y permitirá establecer una tasa máxima de fuga, en conjunto con la superficie total del sistema a probar. Es importante no superar el limite de presión recomendado en DW-144 para evitar dañar el sistema bajo prueba.</p>
                <p class="text-justify">• Acordar con el usuario final la duración de las pruebas. Un conducto con fugas no pasará con una prueba corta, por otra parte, ejecutar una prueba larga en un conducto con fuga, solo retrasará su reparación. Un período de prueba de 5 a 10 minutos es adecuado y permitirá elevar la productividad de pruebas en una obra.</p>
                <p class="text-justify">Finalmente, es de suma importancia que el equipo de pruebas tenga una calibración vigente para asegurar que los resultados obtenidos serán representativos durante la operación del sistema y que durante las etapas finales de la puesta en marcha y calificación del sistema se prosigan con precauciones de no dañar los sistemas probados para garantizar que el valor de fuga obtenido en la prueba sea mantenido durante la operación del sistema.</p>
